class Role {

    /**
     * 
     * @param {Number} id 
     * @param {String} name 
     * @param {String} color 
     * @param {Number} permission 
     */
    constructor(id, name, color, permission){
        this.id = id;
        this.name = name;
        this.color = color;
        this.permission = permission;
    }
}