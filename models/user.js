class User {

    /**
     * 
     * @param {Number} id 
     * @param {String} name 
     * @param {String} email 
     * @param {String} icon 
     * @param {String} password 
     */
    constructor(id, name, email, icon, password) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.icon = icon;
        this.password = password;
        
    }
}